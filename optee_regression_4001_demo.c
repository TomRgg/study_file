/**
 * @brief Performs hash operation tests using TEE cryptographic API
 *
 * Iterates through predefined hash test cases, allocating TEE operations
 * and updating digests for each case. Reports failures through error messages.
 *
 * @note Uses global hash_cases array for test vectors
 * @note Cleans up operations on completion or failure
 */
static void hash_test_ta()
{
    for (n = 0; n < ARRAY_SIZE(hash_cases); n++)
    {
        TEE_Result res = TEE_ERROR_GENERIC;
        TEE_OperationHandle op1 = TEE_HANDLE_NULL;
        TEE_OperationHandle op2 = TEE_HANDLE_NULL;
        uint8_t out[64] = {};
        size_t out_size = 0;

        EMSG("Hash case %d algo 0x%x", (int)n, (unsigned int)hash_cases[n].algo);

        res = TEE_AllocateOperation(&op1, hash_cases[n].algo, TEE_MODE_DIGEST, 0);
        if (res)
        {
            EMSG("TEE_AllocateOperation1 failed with code 0x%x\n", res);
            return res;
        }

        res = TEE_AllocateOperation(&op2, hash_cases[n].algo, TEE_MODE_DIGEST, 0);
        if (res)
        {
            EMSG("TEE_AllocateOperation2 failed with code 0x%x\n", res);
            return res;
        }

        TEE_DigestUpdate(op1, hash_cases[n].in, hash_cases[n].in_incr);

        TEE_CopyOperation(op2, op1);

        out_size = hash_cases[n].out_len;
        memset(out, 0, sizeof(out));
        res = TEE_DigestDoFinal(op2, hash_cases[n].in + hash_cases[n].in_incr,
                                hash_cases[n].in_len - hash_cases[n].in_incr,
                                out, &out_size);
        if (res)
            EMSG("First TEE_DigestDoFinal failed with code 0x%x\n", res);

        TEE_ResetOperation(op1);
        out_size = hash_cases[n].out_len;
        memset(out, 0, sizeof(out));
        res = TEE_DigestDoFinal(op1, hash_cases[n].in,
                                hash_cases[n].in_len,
                                out, &out_size);
        if (res)
            EMSG("Second TEE_DigestDoFinal failed with code 0x%x\n", res);

        /*
         * Invoke TEE_DigestDoFinal() a second time to check that state
         * was properly reset
         */
        out_size = hash_cases[n].out_len;
        memset(out, 0, sizeof(out));
        res = TEE_DigestDoFinal(op1, hash_cases[n].in,
                                hash_cases[n].in_len,
                                out, &out_size);
        if (res)
            EMSG("Third TEE_DigestDoFinal failed with code 0x%x\n", res);

        TEE_FreeOperation(op1);
        TEE_FreeOperation(op2);
    }
    return res;
}

static void tee_signature_invalid_test()
{
    TEE_Result res = TEE_ERROR_GENERIC;
    size_t key_size = 512;
    TEE_ObjectHandle key = TEE_HANDLE_NULL;
    TEE_OperationHandle ops = TEE_HANDLE_NULL;
    TEE_OperationHandle opv = TEE_HANDLE_NULL;
    TEE_OperationHandle ope = TEE_HANDLE_NULL;
    TEE_OperationHandle opd = TEE_HANDLE_NULL;
    uint32_t ret_orig = 0;
    uint8_t in[TEE_SHA1_HASH_SIZE] = {};
    uint8_t out[1024] = {};
    uint8_t tmp[1024] = {};
    size_t out_size = 0;
    size_t tmp_size = 0;
    size_t n = 0;
    size_t m = 0;
    size_t i = 0;

    TEE_GenerateRandom(in, sizeof(in));
    res = TEE_AllocateTransientObject(TEE_TYPE_RSA_KEYPAIR, key_size, &key);
    if (res)
    {
        EMSG("TEE_AllocateTransientObject failed with code 0x%x\n", res);
        return;
    }

    res = TEE_GenerateKey(key, key_size, NULL, 0);
    if (res)
    {
        EMSG("TEE_GenerateKey failed with code 0x%x\n", res);
        return;
    }

    res = TEE_AllocateOperation(&ops, TEE_ALG_RSASSA_PKCS1_V1_5_SHA1, TEE_MODE_SIGN, key_size);
    if (res)
    {
        EMSG("TEE_AllocateOperation TEE_MODE_SIGN failed with code 0x%x\n", res);
        return;
    }

    res = TEE_AllocateOperation(&opv, TEE_ALG_RSASSA_PKCS1_V1_5_SHA1, TEE_MODE_VERIFY, key_size);
    if (res)
    {
        EMSG("TEE_AllocateOperation TEE_MODE_VERIFY failed with code 0x%x\n", res);
        return;
    }

    res = TEE_AllocateOperation(&ope, TEE_ALG_RSA_NOPAD, TEE_MODE_ENCRYPT, key_size);
    if (res)
    {
        EMSG("TEE_AllocateOperation TEE_MODE_ENCRYPT failed with code 0x%x\n", res);
        return;
    }

    res = TEE_AllocateOperation(&opd, TEE_ALG_RSA_NOPAD, TEE_MODE_DECRYPT, key_size);
    if (res)
    {
        EMSG("TEE_AllocateOperation TEE_MODE_DECRYPT failed with code 0x%x\n", res);
        return;
    }

    /*
     * The core of the test case is inspired by the one in libtomcrypt:
     * https://github.com/libtom/libtomcrypt/blob/6ad52252688bb34f90b5e79da4830a927e87b81f/testprof/rsa_test.c#L398
     *
     * Testcase for Bleichenbacher attack
     *
     * (1) Create a valid signature
     * (2) Check that it can be verified
     * (3) Transform the package to fetch plain text (using the encrypt
     *     operation in GP TEE Internal API)
     * (4) Forge the structure of PKCS#1-EMSA encoded data
     * (4.1) Search for start and end of the padding string
     * (4.2) Move the signature to the front of the padding string
     * (4.3) Zero the message until the end
     * (5) Transform the package back (using the decrypt operation in
     *     GP TEE Internal API)
     * (6) The result should not be valid if the implementation is robust.
     */
    for (i = 0; i < 9; i++)
    {
        EMSG("Iteration %zu", i);

        /* 1 */
        out_size = sizeof(out);
        res = TEE_AsymmetricSignDigest(ops, NULL, 0, in, sizeof(in), out, &out_size);
        if (res)
        {
            EMSG("TEE_AsymmetricSignDigest failed with code 0x%x\n", res);
            return;
        }

        /* 2 */
        // if (!ADBG_EXPECT_TEEC_SUCCESS(c,
        //                               ta_crypt_cmd_asymmetric_verify(c, &s, opv, NULL, 0,
        //                                                              in, sizeof(in), out, out_size)))
        //     goto out;
        res = TEE_AsymmetricVerifyDigest(opv, NULL, 0, in, sizeof(in), out, out_size);
        if (res)
        {
            EMSG("TEE_AsymmetricVerifyDigest failed with code 0x%x\n", res);
            return;
        }

        /* 3 */
        tmp_size = sizeof(tmp);
        // if (!ADBG_EXPECT_TEEC_SUCCESS(c,
        //                               ta_crypt_cmd_asymmetric_encrypt(c, &s, ope, NULL, 0,
        //                                                               out, out_size, tmp, &tmp_size)))
        //     goto out;
        res = TEE_AsymmetricEncrypt(ope, NULL, 0, out, out_size, tmp, &tmp_size);
        if (res)
        {
            EMSG("TEE_AsymmetricEncrypt failed with code 0x%x\n", res);
            return;
        }

        /* 4.1 */
        for (n = 0; n < tmp_size - i; n++)
            if (tmp[n] == 0xff)
                break;

        /* Shall find at least a padding start before buffer end */
        // if (!ADBG_EXPECT_COMPARE_UNSIGNED(c, n, <, tmp_size - i - 1))
        //     goto out;
        if (n >= temp_size - i - 1)
        {
            EMSG("No padding start found\n");
            return;
        }

        for (m = n + 1; m < tmp_size; m++)
            if (tmp[m] != 0xff)
                break;

        /* 4.2 */
        memmove(tmp + n + i, tmp + m, tmp_size - m);

        /* 4.3 */
        n = n + i + tmp_size - m;

        /* Prevent overrun when zeroing buffer end */
        // if (!ADBG_EXPECT_COMPARE_UNSIGNED(c, n, <=, tmp_size))
        //     goto out;
        if (n > tmp_size)
        {
            EMSG("Buffer overrun\n");
            return;
        }

        memset(tmp + n, 0, tmp_size - n);

        /* 5 */
        out_size = sizeof(out);
        // if (!ADBG_EXPECT_TEEC_SUCCESS(c,
        //                               ta_crypt_cmd_asymmetric_decrypt(c, &s, opd, NULL, 0,
        //                                                               tmp, tmp_size, out, &out_size)))
        //     goto out;
        res = TEE_AsymmetricDecrypt(opd, NULL, 0, tmp, tmp_size, out, &out_size);
        if (res)
        {
            EMSG("TEE_AsymmetricDecrypt failed with code 0x%x\n", res);
            return;
        }

        /* 6 */
        // if (!ADBG_EXPECT_TEEC_RESULT(c, TEE_ERROR_SIGNATURE_INVALID,
        //                              ta_crypt_cmd_asymmetric_verify(c, &s, opv, NULL, 0,
        //                                                             in, sizeof(in), out, out_size)))
        //     goto out;
        res = TEE_AsymmetricVerifyDigest(opv, NULL, 0, in, sizeof(in), out, out_size);
        if (res != TEE_ERROR_SIGNATURE_INVALID)
        {
            EMSG("TEE_AsymmetricVerifyDigest should have failed(TEE_ERROR_SIGNATURE_INVALID) with code 0x%x\n", res);
            return;
        }
    }
}

